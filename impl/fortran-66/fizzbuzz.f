      PROGRAM FIZZBUZZ
      INTEGER I, N, IFIZZ(1), IBUZZ(1)
      LOGICAL LFIZZ, LBUZZ
      DATA IFIZZ /4Hfizz/, IBUZZ /4Hbuzz/
      READ (5, 100) N
  100 FORMAT (I10)
      DO 600 I = 1, N
      LFIZZ = MOD(I,3) .EQ. 0
      LBUZZ = MOD(I,5) .EQ. 0
      IF (LFIZZ .AND. LBUZZ) GOTO 200
      IF (LFIZZ) GOTO 300
      IF (LBUZZ) GOTO 400
      GOTO 500
  200 WRITE (6,210) IFIZZ, IBUZZ
  210 FORMAT (1A4,1H ,1A4)
      GOTO 600
  300 WRITE (6,310) IFIZZ
  310 FORMAT (1A4)
      GOTO 600
  400 WRITE (6,310) IBUZZ
      GOTO 600
  500 WRITE (6,510) I
  510 FORMAT (I0)
      GOTO 600
  600 CONTINUE
      STOP
      END PROGRAM FIZZBUZZ
